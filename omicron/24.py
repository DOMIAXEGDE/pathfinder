def apply_cli_overrides(compose_config: Dict[str, Any], args: argparse.Namespace) -> Dict[str, Any]:
    config = copy.deepcopy(compose_config)
    for dotted, value in args.set or []:
        set_path(config, dotted, parse_json_value(value))
    if args.input_format:
        set_path(config, "input.format", args.input_format)
    source = config.setdefault("source", {})
    filters = source.setdefault("filters", {})
    if args.category:
        filters["categories"] = args.category
        filters["exclude_categories"] = []
    if args.register:
        filters["registers"] = args.register
    if args.ref:
        filters["refs"] = args.ref
    if args.value_regex:
        filters["value_regex"] = args.value_regex
    if args.source_mode:
        source["mode"] = args.source_mode
    pointer = source.setdefault("pointer", {})
    if args.pointer_relation:
        pointer["relation"] = args.pointer_relation
    if args.pointer_rule:
        pointer["pointer_rule"] = args.pointer_rule
    if args.source_ref:
        pointer["source_ref"] = args.source_ref
    if args.source_category:
        pointer["source_category"] = args.source_category
    if args.target_category:
        pointer["target_category"] = args.target_category
    if args.output or args.format or args.entry_template or args.join is not None:
        output_cfg = copy.deepcopy(config.get("outputs", [{}])[0] if config.get("outputs") else {})
        if args.output is not None:
            output_cfg["path"] = args.output
        if args.format is not None:
            output_cfg["format"] = args.format
        template = output_cfg.setdefault("template", {})
        if args.entry_template is not None:
            template["entry"] = args.entry_template
        if args.join is not None:
            template["join"] = args.join
        config["outputs"] = [output_cfg]
    return config


def main(argv: Optional[List[str]] = None) -> int:
    args = build_arg_parser().parse_args(argv)

    if args.write_default_config:
        Path(args.write_default_config).write_text(json.dumps(DEFAULT_COMPOSE_CONFIG, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
        return 0

    tensor_config = load_tensor_config(Path(args.tensor_config) if args.tensor_config else DEFAULT_TENSOR_CONFIG_PATH)
    compose_config = load_compose_config(Path(args.config) if args.config else None)
    compose_config = apply_cli_overrides(compose_config, args)

    if args.write_effective_config:
        Path(args.write_effective_config).write_text(json.dumps(compose_config, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")

    if not args.input:
        raise ValueError("No input supplied. Use --input with a formatted file from format_paste.py.")

    workspace = load_workspace(
        args.input,
        input_format=compose_config.get("input", {}).get("format", "auto"),
        tensor_config=tensor_config,
        merge_mode=compose_config.get("input", {}).get("merge_mode", "replace"),
    )
    workspace = infer_missing_metadata(workspace, compose_config, tensor_config)
    workspace = run_plugins(workspace, compose_config, "before_outputs")

    outputs = compose_config.get("outputs", []) or [{"format": args.format or "custom", "path": args.output or ""}]
    encoding = tensor_config.get("output", {}).get("encoding", "utf-8")
    for output_cfg in outputs:
        rendered = render_output(workspace, compose_config, tensor_config, output_cfg)
        write_text(str(output_cfg.get("path", "")), rendered, encoding=encoding)
    return 0


