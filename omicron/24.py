"""
compose.py

Local companion tool for the TR Tensor application.

compose.py consumes formatted output from format_paste.py, then renders custom
text/code files or Tensor-compatible files programmatically. Rendering is driven
by a user-authored JSON compose configuration file.

Input formats:
  auto, tensor_text, databank_text, json_workspace, json_records, csv_records,
  jsonl, markdown, dot

Output formats:
  custom, tensor_text, databank_text, json_workspace, json_records,
  csv_records, jsonl, markdown, summary, dot

The "custom" output format uses templates with fields such as:
  {ref}, {value}, {category}, {bank}, {register}, {address}, {ordinal},
  {meta.key}, {meta.parent_key}, {bank_title}, {json}

The pointer-target source mode lets a compose config reuse the systematic
pointer entries generated by format_paste.py. This is useful for rebuilding code
blocks from code_line entries, stitching paragraphs from sentence entries, or
generating entirely new files from category-index pointer batches.
"""

